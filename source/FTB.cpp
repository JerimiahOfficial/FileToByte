#include "../source/FTB.hpp"

#include <format>
#include <fstream>
#include <iomanip>
#include <memory>
#include <sstream>
#include <vector>

namespace FileToByte {

std::string Convert(const std::string &path, const std::string &varName) {
  std::ifstream file(path, std::ios::binary);

  int size;
  file.seekg(0, std::ios::end);
  size = file.tellg();

  std::unique_ptr<char[]> data(new char[size]);
  file.read(data.get(), size);

  std::ostringstream oss;
  oss << "#pragma once\n\n";
  oss << "/*\n";
  oss << " *\tGenerated by File To Byte\n";
  oss << " *\thttps://github.com/JerimiahOfficial/FileToByte\n";
  oss << " */\n\n";
  oss << "const unsigned int " << varName << "_Size = " << size << ";\n";
  oss << "const unsigned int " << varName << "_Data[" << size << "/4] = {";
  oss << std::hex << std::setfill('0');

  int Columns = 0;
  for (int i = 0; i < size; ++i) {
    unsigned int value = static_cast<unsigned char>(data[i]);

    if ((Columns++ % 15) == 0) oss << "\n\t";

    if (i != size - 1)
      oss << "0x" << std::setw(2) << value << ", ";
    else
      oss << "0x" << std::setw(2) << value << "\n";
  }
  oss << "};\n";

  // Cleanup
  return oss.str();
}

}  // namespace FileToByte
